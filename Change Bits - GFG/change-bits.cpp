class Solution {
  public:
    vector<int> changeBits(int N) {
        int val = 0;
        int tmp = N;
        int cnt = 0;
        while(tmp)
        {
            val += (1<<cnt); cnt++;
            // cout << cnt << "\n";
            tmp /= 2;
        }
        
        return {val - N,val};
    }
};


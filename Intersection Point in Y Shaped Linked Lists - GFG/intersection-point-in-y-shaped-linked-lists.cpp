class Solution
{
public:
    int intersectPoint(Node* head1, Node* head2)
    {
        auto tmp1 = head1 , tmp2 = head2;
        bool ok1 = true , ok2 = true;
        while(tmp1 and tmp2)
        {
            if(tmp1 != tmp2)
            {
                if(tmp1->next)
                    tmp1 = tmp1->next;
                else if(ok1)
                    tmp1 = head2 , ok1 = false;
                
                if(tmp2->next)
                    tmp2 = tmp2->next;
                else if(ok2)
                    tmp2 = head1 , ok2 = false;
            }
            else 
                return tmp1->data;
            // cout << tmp1->data << " " << tmp2->data << "\n"; 
        }
        
        return -1;
    }
};



